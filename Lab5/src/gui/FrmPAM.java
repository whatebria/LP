    private void btnNuevoVehiculoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNuevoVehiculoActionPerformed
        int tipo = cboTipo.getSelectedIndex();

        if (tipo == 1) {  //Automovil
            Automovil a = new Automovil();
            a.setPatente(txtPatente.getText().trim().toUpperCase());
            a.setAño(Integer.parseInt(txtAño.getText().trim()));
            a.setDeLujo(chkDeLujo.isSelected());

            pam.registrarVehiculo(a);

        }

        if (tipo == 2) {  //Minibus
            Minibus m = new Minibus();
            m.setPatente(txtPatente.getText().trim().toUpperCase());
            m.setAño(Integer.parseInt(txtAño.getText().trim()));
            m.setCapacidad(Integer.parseInt((txtPasajeros.getText().trim())));

            pam.registrarVehiculo(m);

        }

        txtPatente.setText("");
        txtAño.setText("");
        cboTipo.setSelectedIndex(0);
        chkDeLujo.setSelected(false);
        txtPasajeros.setText("");

        JOptionPane.showMessageDialog(rootPane, "Registro exitoso", "Nuevo Vehículo", JOptionPane.INFORMATION_MESSAGE);

        cargarVehiculosList();
       

    }//GEN-LAST:event_btnNuevoVehiculoActionPerformed

    private void cboTipoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cboTipoActionPerformed
        int tipo = cboTipo.getSelectedIndex();
        lblDeLujo.setVisible(false);
        chkDeLujo.setVisible(false);
        lblPasajeros.setVisible(false);
        txtPasajeros.setVisible(false);
        btnNuevoVehiculo.setEnabled(false);
        // 0 Seleccione 1 Vehiculo 2 Minibus
        switch (tipo) {
            case 1:
                lblDeLujo.setVisible(true);
                chkDeLujo.setVisible(true);
                btnNuevoVehiculo.setEnabled(true);
                break;
            case 2:
                lblPasajeros.setVisible(true);
                txtPasajeros.setVisible(true);
                btnNuevoVehiculo.setEnabled(true);
                break;

        }
    }//GEN-LAST:event_cboTipoActionPerformed

