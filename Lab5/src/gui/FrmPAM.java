    private void cargarVehiculosList() {
        ArrayList<Vehiculo> vehiculos = pam.getVehiculos();
        if (vehiculos.size() > 0) {
            DefaultComboBoxModel model = new DefaultComboBoxModel();

            for (Vehiculo vehiculo : vehiculos) {
                model.addElement(vehiculo);
            }
            lstVehiculos.setModel(model);
            cboVehiculos.setModel(model);
        }

    }
